<h3>Khi bạn quảng cáo trên Google - Bizweb sẽ giúp bạn:</h3>
<ul>
	<li>
		<strong>Tư vấn lập kế hoạch quảng cáo Google Adwords theo quy trình chuyên nghiệp:</strong> 
		Với những chuyên gia hàng đầu về quảng cáo trực tuyến và quảng cáo trên Google, 
		chúng tôi sẽ tìm hiểu lĩnh vực kinh doanh của doanh nghiệp và tư vấn cho khách hàng lựa chọn <strong>từ khóa, 
		ngân sách</strong>, thời gian cho <strong>chiến dịch quảng cáo trên Google</strong>, phù hợp với sản phẩm/dịch vụ của doanh nghiệp bạn.
	</li>
	<li>
		<strong>Tạo quảng cáo Google Adwords:</strong> Sau khi lập kế hoạch, chúng tôi sẽ tiến hành tạo <strong>chiến dịch quảng cáo</strong> trên Google AdWords cho doanh nghiệp.
	</li>
	<li>
		<strong>Giám sát quảng cáo Google Adwords :</strong> 
		Chúng tôi sẽ cùng bạn giám sát hoạt động của <strong>chiến dịch quảng cáo trên Google</strong> thông qua việc theo dõi <strong>chất lượng từ khóa</strong> 
		(hiển thị quảng cáo, <strong>tỷ lệ nhấp chuột</strong>), kiểm tra biến động của giá <strong>từ khóa</strong> và tác động để cải thiện <strong>chất lượng từ khóa</strong> 
		(Quality Score) sao cho bạn luôn có mức đặt giá (bid) lợi nhất. 
		Công tác này được làm thường xuyên nhiều lần mỗi ngày để đảm bảo chiến dịch luôn vận hành hiệu quả nhất.
	</li>
	<li>
		<strong>Báo cáo hiệu quả của chiến dịch quảng cáo trên Google:</strong> 
		Báo cáo chi tiết cho bạn định kỳ (tuần/tháng/quý) hoặc bất kì lúc nào khi bạn yêu cầu. 
		Kết quả báo cáo sẽ giúp bạn biết được những thông tin hữu ích như số lần hiển thị <strong>quảng cáo trên Google</strong>, số lần nhấp chuột, 
		hành động của khách trong quá trình thăm quan website (convergence tracking). 
		Báo cáo cũng ghi rõ những thông tin chi tiết về khách hàng của bạn như: 
		quốc gia, đến theo từ khóa nào, truy cập thời điểm nào trong ngày, nội dung nào trên web được khách ưa thích nhất và nhiều thông tin hữu ích khác.
	</li>
</ul>