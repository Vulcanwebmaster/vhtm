                    <h1>
                        Chào đón sự nồng nhiệt!</h1>
                    <p>
                        <strong>Duy trì cố gắng và đúng trong khi mạo hiểm,</strong><br>
                        getreu dieser Philosophie bietet Ihnen die DEH ein vielfältiges Sortiment feinster
                        Backwaren und innovativer Produktneuheiten an. Tauchen Sie ein in unser Backwarenparadies
                        und erleben Sie die große Vielfalt an knusprigen Brötchen, rustikalen Broten, süßen
                        Feingebäcken und vielen weiteren Backwaren...</p>
                    <p>
                        <strong>Exklusive Raffinesse </strong>
                        <br>
                        Exklusiv bei DEH: Griechische Kalamata-Oliven verleihen dem 425 Gramm schweren <a
                            href="javascript:if(confirm(''))window.location='olivenbrot-425g-halbgebacken'"
                            tppabs="olivenbrot-425g-halbgebacken"><strong>Olivenbrot</strong></a> die besondere
                        mediterrane Note. Das halbgebackene Brot ist mit seiner handwerklichen Optik ein
                        idealer Begleiter zum edlen Menü. Gleiches gilt für das herzhaft-aromatische, halbgebackene
                        <a href="javascript:if(confirm(''))window.location='walnussbroetchen-90g-halbgebacken'"
                            tppabs="walnussbroetchen-90g-halbgebacken"><strong>Walnussbrötchen</strong></a>.
                        Raffiniert im Geschmack und rustikal in der Optik wertet das 80 Gramm schwere Premium-Brötchen
                        das Sortiment auf.</p>
                    <p>
                        Das neue <a href="javascript:if(confirm(''))window.location=''" tppabs="alpenkorn-250g-halbgebacken">
                            <strong>Alpenkorn</strong></a> erhält im Steinofen sein besonderes Aroma und
                        das appetitliche handwerkliche Aussehen. Das halbgebackene, 250 Gramm schwere Stangengebäck
                        ist mit brauner Leinsaat und Haferflocken bestreut. Sojabohnen verleihen der Krume
                        die gewisse Saftigkeit und einen nussigen Geschmack.</p>
                    <p>
                        <strong>Umsatzstark in den Tag starten</strong><br>
                        Als raffiniert belegter Snack hat der neue <a href="javascript:if(confirm(''))window.location='sesam-laugenbagel-60g-fertig-gebacken'"
                            tppabs="sesam-laugenbagel-60g-fertig-gebacken"><strong>Sesam-Laugenbagel</strong></a>
                        das Zeug zum neuen Umsatzrenner. Der 60 Gramm schwere Laugenbagel ist mit Sesam
                        bestreut und wird fertig gebacken im Karton zu 40 Stück geliefert.</p>
                    <p>
                        Neu und exklusiv bei DEH zu beziehen sind die <a href="javascript:if(confirm(''))window.location='berliner/premium-berliner-schokocreme-90g-fertig-gebacken'"
                            tppabs="premium-berliner-schokocreme-90g-fertig-gebacken"><strong>Premium Berliner</strong></a>
                        in den Varianten <a href="javascript:if(confirm('))window.location='premium-berliner-schokocreme-90g-fertig-gebacken'"
                            tppabs="premium-berliner-schokocreme-90g-fertig-gebacken"><strong>Schokolade </strong>
                        </a>und<strong> <a href="javascript:if(confirm(''))window.location='premium-berliner-vanillecreme-94g-fertig-gebacken'"
                            tppabs="premium-berliner-vanillecreme-94g-fertig-gebacken">Vanille</a></strong>.
                        Der 90 Gramm schwere Schoko-Premium Berliner verführt mit seiner zartschmelzenden
                        Schokoladencreme-Füllung. Der Berliner ist dunkel glasiert und effektvoll mit grob
                        gehackten Vollmilch-, Zartbitter- und weißen Schokoladestückchen veredelt. 94 Gramm
                        wiegt der leckere Vanille-Premium Berliner mit einer zarten Vanillecremefüllung,
                        heller Glasur und feinen Vollmilchschokodrops.</p>
                    <p>
                        Ein optisches Highlight ist der neue <a href="javascript:if(confirm(''))window.location='pinky-donut-58g-fertig-gebacken'"
                            tppabs="donuts/pinky-donut-58g-fertig-gebacken"><strong>Pinky Donut</strong></a>
                        – überzogen mit einer verführerischen Erdbeer-Zuckerglasur und dekoriert mit Zuckerstreuseln,
                        ist der 58 Gramm schwere Donut nicht nur ein echter Hingucker sondern auch ein süßer
                        Leckerbissen!</p>
                    <p>
                        Unsere Backwaren erhalten Sie natürlich in unterschiedlichen Convenience-Graden.
                        Sowohl der rohe Teigling, als auch vorgegarte, halbgebackene sowie fertig gebackene
                        Produkte werden von uns mit größter Sorgfalt hergestellt und dann bei Bedarf mit
                        feinsten Saaten veredelt.</p>
                    <p>
                        Unsere <strong><a href="javascript:if(confirm(''))window.location='non-food-produkte'"
                            tppabs="non-food-produkte">Non-Food</a>-</strong> und&nbsp;<a href="javascript:if(confirm(''))window.location=''"
                                tppabs="http://www.entrup-haselbach.de/werbezubehoer"><strong>Werbeprodukte</strong></a>
