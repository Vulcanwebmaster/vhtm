<?php
	class Admin extends Admin_Controller
	{
		private $module;
		function __construct()
		{
			parent::__construct();
			$this->module=strtolower(get_class());
			
			$this->load->model('Mtagcloud');
			$this->load->library('form_validation');
			$this->load->library('session');
			
			//=============================================
			$this->load->library('Ckeditor',array('instanceName' => 'CKEDITOR1','basePath' => base_url()."assets/niwcom/ckeditor/", 'outPut' => true));                             
			//=============================================
		}
		
		function index()
		{
			$data['title']='TagCloud';
			$data['bcCurrent']='TagCloud';
			$data['list']=$this->Mtagcloud->getListFull('tagcloud');
			$data['module']=$this->module;
			$data['page']='admin_vlist';
			$this->load->view('admin/container',$data);
		}
		
		function _input()
		{
			$input=array('tentag'=>$this->input->post('tentag'),
						'tentage'=>$this->input->post('tentage'),
						'hot'=>$this->input->post('hot'),
						'alias'=>$this->getAliasByName($this->input->post('tentag')),
						);
			return $input;
		}
		
		function insert()
		{
			if (!$this->input->post('tentag'))
			{
				$data['config'] = $this->setupCKEditor('97%','200px');
				$data['title']='Thêm TagCloud';
				$data['bcCurrent']='TagCloud';
				$data['module']=$this->module;
				$data['page']='admin_vinsert';
				$this->load->view('admin/container',$data);
			}
			else 
			{
				$this->form_validation->set_rules('tentag','Tên','required|trim');
				
				$this->form_validation->set_message('required','Mục %s không được bỏ trống');
				
				if ($this->form_validation->run())
				{
					$input=$this->_input();
					if ($this->Mtagcloud->insertNewRow('tagcloud',$input))
					{
						$this->session->set_userdata('result','Thêm mới thành công');
					}
					else $this->session->set_userdata('result','Thêm mới không thành công');
					$this->index();
				}
				else 
				{
					$data['config'] = $this->setupCKEditor('97%','200px');
					$data['title']='Thêm TagCloud';
					$data['bcCurrent']='TagCloud';
					$data['module']=$this->module;
					$data['page']='admin_vinsert';
					$this->load->view('admin/container',$data);
				}
			}
		}
		
		function edit($id=0)
		{
			//=============================================
			$data['config'] = $this->setupCKEditor('97%','200px');
			//=============================================
			if (!$this->input->post('tentag'))
			{
				$data['info']=$this->Mtagcloud->getRowByColumn('tagcloud','id',$id);
				$data['title']='Sửa TagCloud';
				$data['bcCurrent']='TagCloud';
				$data['module']=$this->module;
				$data['page']='admin_vedit';
				$this->load->view('admin/container',$data);
			}
			else 
			{
				$this->form_validation->set_rules('tentag','Tên Tag (Việt)','required|trim');
				
				$this->form_validation->set_message('required','Mục %s không được bỏ trống');
				
				if ($this->form_validation->run())
				{
					$input=$this->_input();
					if ($this->Mtagcloud->updateRowByColumn('tagcloud','id',$id,$input))
					{
						$this->session->set_userdata('result','Cập nhật thành công');
					}
					else $this->session->set_userdata('result','Cập nhật không thành công');
					$this->index();
				}
				else 
				{
					$data['info']=$this->Mtagcloud->getRowByColumn('tagcloud','id',$id);
					$data['title']='Sửa TagCloud';
					$data['bcCurrent']='TagCloud';
					$data['module']=$this->module;
					$data['page']='admin_vedit';
					$this->load->view('admin/container',$data);
				}
			}
		}
		
		function delete($id=0)
		{
			if ($this->Mtagcloud->deleteRowByColumn('tagcloud','id',$id))
			{
				$this->session->set_userdata('result','Xóa thành công');
			}
			else $this->session->set_userdata('result','Xóa không thành công');		
			$this->index();
		}
	}
?>